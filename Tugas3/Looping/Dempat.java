//Buatlah looping untuk perkalian dengan output dibawah ini
package Tugas3.Looping;

public class Dempat {
    public static void main(String[] args) {
        int perkalian = 5;

        for (int i = 6; i <= 10; i++) {
            int hasil = perkalian * i;
            System.out.println(perkalian + " x " + i + " = " + hasil);
        }
    }
}
