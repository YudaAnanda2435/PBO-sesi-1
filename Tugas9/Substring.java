package Tugas9;

public class Substring {
    public static void main(String[] args) {
        String lapar = "aku mau nasi padang";
        System.out.println("Hasilnya adalah : " + lapar.substring(2, 6));
        System.out.println("Hasilnya adalag : " + lapar.substring(4, lapar.length()));
    }
}
